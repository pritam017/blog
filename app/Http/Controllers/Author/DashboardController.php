     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $user =Auth::user();
        $posts = $user->posts;
        $popular_post = $user->posts()->withCount('favorite_to_users')
        ->orderBy('view_count', 'desc')
        ->orderBy('favorite_to_users_count')
        ->take(5)->get();
        $total_pending_posts = $posts->where('is_approved', false)->count();
        $all_view = $posts->sum('view_count');
       return view('author.dashboard', compact('posts','popular_post','total_pending_posts','all_view'));
    }

